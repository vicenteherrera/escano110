<p>
Escaño110 es una plataforma web que fomenta la participación ciudadana en la vida política andaluza, 
propiciando una mayor comunicación entre movimientos ciudadanos y gestores públicos. 
</p><p>
Con tal fin, facilita la recogida de firmas electrónicas para la presentación de <b>Iniciativas Legislativas 
Populares (ILPs)</b> al Parlamento de Andalucía por parte de la ciudadanía, a título personal o como colectivo. 
</p><p>
Dada la complejidad de la presentación de una ILP, la plataforma también está abierta a otros niveles de 
participación y compromiso. La web permite que cualquier usuario pueda enviar <b>preguntas de iniciativa ciudadana</b>, 
como así lo estipula la ley, y comprobar cuánto tardan los parlamentarios en responder. Asimismo, la ciudadanía 
puede lanzar <b>propuestas</b> no vinculantes para recabar apoyos en esta plataforma dedicada exclusivamente a la 
vida política andaluza. 
</p><p>
Queremos dotar al proyecto de un espíritu crítico y exigente con la política en Andalucía. Creemos en la
 democracia responsable, pero también creemos necesario establecer mecanismos de control y vigilancia sobre 
 lo que los parlamentarios hacen o dejan de hacer. 
</p><p>
Al mismo tiempo, esta plataforma cumple una labor de divulgación comunicativa y educativa, centrada sobre 
todo en aspectos de la vida política y la tramitación legislativa en la región andaluza, temas poco habituales
en los medios de información. Un tratamiento informativo que, a través del <b>blog</b> de Escaño110, da voz a las
 principales reivindicaciones sociales canalizadas a través de esta plataforma web. 
</p><p>
En cuanto a contenidos, el mensaje lo articulamos no solo publicando textos sobre las iniciativas en sí mismas,
 sino también reportajes incisivos sobre temas de participación, transparencia y cuestiones relativas a la vida 
 política andaluza, que abren el debate. 
</p><p>
Finalmente, Escaño 110 también facilita <b>asesoramiento jurídico</b> a personas o colectivos que quieran presentar una
 ILP o iniciar cualquier otro trámite de participación en el Parlamento andaluz. 
</p><p>
Esperamos que la puesta en marcha de esta plataforma incite al desarrollo de iniciativas similares en otras comunidades 
autónomas, trasladando el fomento de la e-democracia y la participación ciudadana a través de las TIC que aquí defendemos.
</p>
